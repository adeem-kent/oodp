
public class Person {

	// instance variables
	int id;
	public String name;
	public String address;
	public double number;

	///// instance method
	public void display() {
		System.out.println("ID: " + id);
		System.out.println("Name: " + name);
		System.out.println("Address: " + address);
		System.out.println("Number: " + number);
	}
}
